package com.woobotech.service.web.dto;

public class BrdFileDTO {

  private String brd_no;
  private String seq;
  private String file_nm;
  private String org_file_nm;
  private String reg_date;

  public String getBrd_no() {
    return brd_no;
  }

  public void setBrd_no(String brd_no) {
    this.brd_no = brd_no;
  }

  public String getSeq() {
    return seq;
  }

  public void setSeq(String seq) {
    this.seq = seq;
  }

  public String getFile_nm() {
    return file_nm;
  }

  public void setFile_nm(String file_nm) {
    this.file_nm = file_nm;
  }

  public String getOrg_file_nm() {
    return org_file_nm;
  }

  public void setOrg_file_nm(String org_file_nm) {
    this.org_file_nm = org_file_nm;
  }

  public String getReg_date() {
    return reg_date;
  }

  public void setReg_date(String reg_date) {
    this.reg_date = reg_date;
  }



}
